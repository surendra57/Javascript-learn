// switch statement 
// find a day

let day= 6;

switch (day) {
        case 0:
        console.log("Sunday");
        break;
        case 1:
        console.log("monday");
        break;
        case 2:
        console.log("tuesday");
        break;
        case 3:
        console.log("wednesday");
        break;
        case 4:
        console.log("thrusday");
        break;
        case 5:
        console.log("friday");
        break;
        case 6:
        console.log("Saturday");
        break;
    default:
        console.log("invalid day")
        break;
}
